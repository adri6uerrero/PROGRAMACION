package POO_II_2;

public class automovil {
	private String marca;

	private String modelo;

	// Constructor

	public automovil(String marca, String modelo) {
		this.marca = marca;
		this.modelo = modelo;
	}
	/* getters y setters */
	public String getMarca() {
		return marca;
	}

	public void setMarca(String marca) {
		this.marca = marca;

	}

	public String getModelo() {

		return modelo;

	}
}
